    class LRUCache {
        int capacity;
        HashMap<Integer, Node> map;
        DubboList list;

        public LRUCache(int capacity) {
            this.capacity = capacity;
            map = new HashMap<>();
            list = new DubboList();
        }

        public int get(int key) {
            if (!map.containsKey(key)) {
                return -1;
            }
            int value = map.get(key).value;
            put(key, value);
            return value;
        }

        public void put(int key, int value) {
            Node node = new Node(key, value);
            if (map.containsKey(key)) {
                list.remove(map.get(key));
            } else {
                if (capacity == list.getSize()) {
                    Node last = list.removeLast();
                    map.remove(last.key);
                }
            }
            list.addFirst(node);
            map.put(key, node);
        }
    }

    class DubboList {
        Node prev;
        Node tail;
        int size;

        public DubboList() {
            prev = new Node();
            tail = new Node();
            prev.next = tail;
            tail.prev = prev;
            this.size = 0;
        }

        public void addFirst(Node x) {
            Node oldHead = prev.next;
            prev.next = x;
            x.prev = prev;
            oldHead.prev = x;
            x.next = oldHead;
            size++;
        }

        public void removeFirst() {
            Node oldHead = prev.next;
            if (oldHead == tail) {
                return;
            }
            Node headNext = oldHead.next;
            prev.next = headNext;
            headNext.prev = prev;
            size--;
        }

        public void remove(Node x) {
            Node xNext = x.next;
            Node xPrev = x.prev;
            xPrev.next = xNext;
            xNext.prev = xPrev;
            size--;
        }

        public Node removeLast() {
            if (tail.prev == prev) {
                return null;
            }
            Node lastNode = tail.prev;
            Node oldPrev = lastNode.prev;
            oldPrev.next = tail;
            tail.prev = oldPrev;
            size--;
            return lastNode;
        }

        public int getSize() {
            return size;
        }

    }

    class Node {

        int key;
        int value;
        Node prev;
        Node next;

        public Node() {
        }

        public Node(int key, int value) {
            this.key = key;
            this.value = value;
        }

        public Node(int key, int value, Node prev, Node next) {
            this.key = key;
            this.value = value;
            this.prev = prev;
            this.next = next;
        }
    }


